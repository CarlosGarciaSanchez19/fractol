#include "libft.h"

int	ft_strisnumber(char *str)
{
	int	count;

	count = 0;
	if (*str == '-' || *str == '+')
		str++;
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
		if (*str == '.' && !count)
		{
			count++;
			str++;
		}
	}
	return (1);
}
